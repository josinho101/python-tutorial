#list
users = ["Bob", "Liza", "Joe", "Hary"]

for x in users:
    print(x)

print("-----------------------------")

#tuple
colors = ("Red", "Green", "Blue")

for x in colors:
    print(x)

print("-----------------------------")

#set
mySet = {1, 2, 3, 4}

for i in mySet:
    print(i)

print("-----------------------------")

#dictionary
employees = {1281: "JJ", 1282: "FF"}

for e in employees:
    print(e, "->", employees[e])

#string
myString = "Hello World"

for x in myString:
    print(x)
