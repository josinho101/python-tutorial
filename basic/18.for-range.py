# iterate till 10
for x in range(10):
    print(x)

print("------------------------")

# specify start range
for x in range(4, 10):
    print(x)

print("------------------------")

#increment by 2
for x in range(5, 20, 2):
    print(x)

print("------------------------")

#else in for loop. will be executed at the end of loop
for x in range(0, 5):
    print(x)
else:
    print("loop completed")
